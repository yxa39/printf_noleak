#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char	*ptr_s1;

	ptr_s1 = (char *)s1;
	while (*ptr_s1)
		ptr_s1++;
	while (*s2)
		*ptr_s1++ = *s2++;
	*ptr_s1 = '\0';
	return (s1);
}
